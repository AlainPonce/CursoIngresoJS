/*
al presionar el botón mostrar 10 repeticiones 
con números ASCENDENTE, desde el 1 al 10.*/
function mostrar()
{
	var iteracion;
	iteracion=0 //arranca en 0
	while(iteracion < 10) // contador siempre menor a 10
	{
		iteracion++; // contador ascendente
		console.log(iteracion);
		alert(iteracion);		
	}	
}
