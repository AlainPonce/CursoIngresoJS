/*
al presionar el botón mostrar 10 repeticiones 
con números DESCENDENTES, desde el 10 al 1.*/
function mostrar()
{
	var iteracion;
	iteracion=11 // contador arranca en 11
	while(iteracion > 1) // cuenta de 11 a 1 inclusive
	{
		iteracion--; // contador descendente
		console.log(iteracion);
		alert(iteracion);
	}
}
